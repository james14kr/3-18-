#판매 정보 등록 테이블 생성

DROP TABLE SALE_INFO;

CREATE TABLE SALE_INFO(
	BUYER VARCHAR(10) NOT NULL
	,COLOR VARCHAR(20) NOT NULL 
	,MODEL_NAME VARCHAR(20) NOT NULL
	,PHONE VARCHAR(13)
	,BUY_DATE DATETIME DEFAULT SYSDATE()
);

SELECT * FROM SALE_INFO;

SELECT SI.BUYER
	,SI.PHONE
	,SI.BUY_DATE
	,SI.COLOR
	,SI.MODEL_NAME
	,CI.PRICE
FROM SALE_INFO SI
JOIN CAR_INFO CI ON SI.MODEL_NAME = CI.MODEL_NAME ;


